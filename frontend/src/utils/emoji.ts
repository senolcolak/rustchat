/**
 * Search emojis by query string
 * Returns array of {name, char} matching the query
 */
export function searchEmojis(query: string, limit: number = 8): Array<{ name: string; char: string }> {
    const cleanQuery = query.toLowerCase().replace(/^:|:$/g, '')
    if (!cleanQuery) return []
    
    const results: Array<{ name: string; char: string; score: number }> = []
    
    for (const [name, char] of Object.entries(emojiMap)) {
        const lowerName = name.toLowerCase()
        let score = 0
        
        // Exact match
        if (lowerName === cleanQuery) {
            score = 100
        }
        // Starts with query
        else if (lowerName.startsWith(cleanQuery)) {
            score = 50
        }
        // Contains query
        else if (lowerName.includes(cleanQuery)) {
            score = 25
        }
        // Query contains name (partial match)
        else if (cleanQuery.includes(lowerName)) {
            score = 10
        }
        
        if (score > 0) {
            results.push({ name, char, score })
        }
    }
    
    // Sort by score descending
    results.sort((a, b) => b.score - a.score)
    
    // Return top matches
    return results.slice(0, limit).map(({ name, char }) => ({ name, char }))
}

/**
 * Gets the Unicode character for a given emoji name.
 * Returns the original name if no match is found.
 */
export function getEmojiChar(name: string): string {
    // Strip colons if present: ":heart:" -> "heart"
    const cleanName = name.replace(/^:|:$/g, '').toLowerCase();
    return emojiMap[cleanName] || name;
}

/**
 * Replaces all :emoji_name: patterns in a string with Unicode characters.
 */
export function replaceEmojiNames(text: string): string {
    return text.replace(/:([a-z0-9_+-]+):/g, (match, name) => {
        const char = emojiMap[name.toLowerCase()];
        return char || match;
    });
}
